const LAYOUT: Layout = Layout::builder()
    .prg_rom_max_size(128 * KIBIBYTE)
    .override_prg_bank_register(R, -1)
    .prg_layout(&[
        PrgWindow::new(0x6000, 0x7FFF,  8 * KIBIBYTE, PrgBank::ABSENT),
        // Use "R" instead of "P" in order to ignore MMC3's normal PRG banking.
        PrgWindow::new(0x8000, 0xFFFF, 32 * KIBIBYTE, PrgBank::ROM.switchable(R)),
    ])
    .chr_rom_max_size(256 * KIBIBYTE)
    .chr_layout(mmc3::CHR_BIG_WINDOWS_FIRST)
    .chr_layout(mmc3::CHR_SMALL_WINDOWS_FIRST)
    .name_table_mirrorings(&[
        NameTableMirroring::VERTICAL,
        NameTableMirroring::HORIZONTAL,
    ])
    // The only known game has horizontal mirroring at start up, but the ROM tries to set it as vertical,
    // so force horizontal here.
    .cartridge_selection_name_table_mirrorings([
        Some(NameTableMirroring::HORIZONTAL),
        Some(NameTableMirroring::HORIZONTAL),
        Some(NameTableMirroring::HORIZONTAL),
        Some(NameTableMirroring::HORIZONTAL),
    ])
    .build();

