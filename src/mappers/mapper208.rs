// Street Fighter IV
pub struct Mapper208 {
    mmc3: mmc3::Mapper004Mmc3,
    protection_registers: [u8; 4],
    protection_table_index: usize,
}

impl Mapper for Mapper208 {
    fn peek_register(&self, _bus: &Bus, addr: CpuAddress) -> ReadResult {
        if matches!(*addr, 0x5800..=0x5FFF) {
            ReadResult::full(self.protection_registers[*addr as usize & 0b11])
        } else {
            ReadResult::OPEN_BUS
        }
    }

    fn write_register(&mut self, bus: &mut Bus, addr: CpuAddress, value: u8) {
        match *addr {
            0x4800..=0x4FFF | 0x6800..=0x6FFF => {
                let fields = splitbits!(min=u8, value, "..mr ...r");
                bus.set_name_table_mirroring(fields.m);
                bus.set_prg_register(R, fields.r);
            }
            0x5000..=0x57FF => {
                self.protection_table_index = value.into();
            }
            0x5800..=0x5FFF => {
                self.protection_registers[*addr as usize & 0b11] = value ^ PROTECTION_TABLE[self.protection_table_index];
            }
            0x8000..=0x9FFF if addr.is_multiple_of(2) => {
                // Force PRG layout to 0 since this mapper doesn't have a second layout, unlike MMC3.
                self.mmc3.write_register(bus, addr, value & 0b1011_1111);
            }
            0x0000..=0x47FF | 0x6000..=0x67FF | 0x7000..=0x9FFF | 0xC000..=0xFFFF => {
                // Normal MMC3 behaviors.
                self.mmc3.write_register(bus, addr, value);
            }
            0xA000..=0xBFFF => { /* This MMC3 functionality has been replaced or ignored by this mapper. */ }
        }
    }

    fn on_end_of_ppu_cycle(&mut self) {
        self.mmc3.on_end_of_ppu_cycle();
    }

    fn on_ppu_address_change(&mut self, bus: &mut Bus, address: PpuAddress) {
        self.mmc3.on_ppu_address_change(bus, address);
    }

    fn irq_counter_info(&self) -> Option<IrqCounterInfo> {
        self.mmc3.irq_counter_info()
    }

    fn layout(&self) -> Layout {
        LAYOUT
    }
}

impl Mapper208 {
    pub fn new() -> Self {
        Self {
            // TODO: Determine correct IRQ behavior. The standard 4 MMC3 behaviors don't work.
            mmc3: mmc3::Mapper004Mmc3::new(Mmc3IrqState::SHARP_IRQ_STATE),
            protection_registers: [0x59; 4],
            protection_table_index: 0,
        }
    }
}